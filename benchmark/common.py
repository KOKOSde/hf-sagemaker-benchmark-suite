def percentile(values: list[float], p: float) -> float:
    """
    Compute percentile with linear interpolation (like numpy.percentile with default method).
    """
    if not values:
        raise ValueError("values is empty")
    if not (0.0 <= p <= 100.0):
        raise ValueError("p must be in [0, 100]")
    xs = sorted(values)
    if len(xs) == 1:
        return xs[0]
    k = (len(xs) - 1) * (p / 100.0)
    f = int(k)
    c = min(f + 1, len(xs) - 1)
    if f == c:
        return xs[f]
    d0 = xs[f] * (c - k)
    d1 = xs[c] * (k - f)
    return d0 + d1


@dataclass(frozen=True)
class LatencySummary:
    n: int
    concurrency: int
    p50_ms: float
    p95_ms: float
    p99_ms: float
    mean_ms: float
    stdev_ms: float
    error_count: int


@dataclass(frozen=True)
class ThroughputSummary:
    duration_seconds: float
    concurrency: int
    requests: int
    successes: int
    errors: int
    rps: float


def write_results(path: str | Path, record: dict[str, Any]) -> None:
    path = Path(path)
    if path.exists():
        data = json.loads(path.read_text(encoding="utf-8"))
    else:
        data = {"note": "Generated by benchmark scripts", "results": []}
    data.setdefault("results", [])
    data["results"].append(record)
    path.write_text(json.dumps(data, indent=2, sort_keys=True) + "\n", encoding="utf-8")


class SageMakerRuntimeClient:
    def __init__(self, region: str):
        self._client = boto3.client("sagemaker-runtime", region_name=region)

    def invoke(self, endpoint_name: str, payload: bytes, content_type: str = "application/json", accept: str = "application/json") -> bytes:
        resp = self._client.invoke_endpoint(
            EndpointName=endpoint_name,
            ContentType=content_type,
            Accept=accept,
            Body=payload,
        )
        return resp["Body"].read()


def now_ms() -> float:
    return time.perf_counter() * 1000.0


def summarize_latencies(latencies_ms: list[float], concurrency: int, error_count: int) -> LatencySummary:
    mean_ms = statistics.mean(latencies_ms) if latencies_ms else float("nan")
    stdev_ms = statistics.pstdev(latencies_ms) if len(latencies_ms) > 1 else 0.0
    return LatencySummary(
        n=len(latencies_ms) + error_count,
        concurrency=concurrency,
        p50_ms=percentile(latencies_ms, 50.0) if latencies_ms else float("nan"),
        p95_ms=percentile(latencies_ms, 95.0) if latencies_ms else float("nan"),
        p99_ms=percentile(latencies_ms, 99.0) if latencies_ms else float("nan"),
        mean_ms=mean_ms,
        stdev_ms=stdev_ms,
        error_count=error_count,
    )


def summarize_throughput(duration_seconds: float, concurrency: int, requests: int, successes: int, errors: int) -> ThroughputSummary:
    rps = successes / duration_seconds if duration_seconds > 0 else 0.0
    return ThroughputSummary(
        duration_seconds=duration_seconds,
        concurrency=concurrency,
        requests=requests,
        successes=successes,
        errors=errors,
        rps=rps,
    )

